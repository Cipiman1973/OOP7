public class User {
    private String firstName;
    private String lastName;
    private int age;
    private String userName;
    private String password;
    private String gender;
    private String Phone;

    public static setFirstName(String firstName) {
        This.firstName = firstName;
    }

    public static setLastName(String lastName) {
        This.lastName = lastName;
    }

    public static setAge(int age) {
        This.age = age;
    }

    public static setUserName(String userName) {
        This.userName = userName;
    }

    public static setPasswor(String password) {
        This.password = password;
    }

    public static setGender(String gender) {
        This.gender = gender;
    }

    public static setPhone(String Phone) {
        This.Phone = Phone;
    }


}